# Evaluation
# ---------------------------------------------------------------------------

def main():
    args = parse_args()
    os.makedirs(args.output_dir, exist_ok=True)

    device = "cuda" if torch.cuda.is_available() else "cpu"
    print(f"\nDevice : {device}")
    if device == "cuda":
        print(f"GPU    : {torch.cuda.get_device_name(0)}\n")

    # ── Load model ────────────────────────────────────────────────────────
    print(f"Loading model from {args.model_dir} ...")
    tokenizer = DistilBertTokenizerFast.from_pretrained(args.model_dir)
    model = DistilBertForSequenceClassification.from_pretrained(args.model_dir)
    model.to(device)

    # Use id2label from model config if available; fall back to LABELS constant
    id2label = getattr(model.config, "id2label", {int(i): l for i, l in enumerate(LABELS)})
    labels = [id2label[i] for i in sorted(id2label)]

    # ── Load test data ─────────────────────────────────────────────────────
    test_path = args.test_path or os.path.join(args.model_dir, "test_set.csv")
    print(f"Loading test set from {test_path} ...")
    test_df = pd.read_csv(test_path)
    test_df = test_df.dropna(subset=["text_representation", "label"]).reset_index(drop=True)
    test_df["text_representation"] = test_df["text_representation"].astype(str)

    # Encode labels if not already present
    label2id_local = {l: i for i, l in enumerate(labels)}
    if "label_id" not in test_df.columns:
        test_df["label_id"] = test_df["label"].map(label2id_local)

    print(f"Test samples : {len(test_df)}")
    print(f"Label distribution:\n{test_df['label'].value_counts().to_string()}\n")

    # ── Inference ─────────────────────────────────────────────────────────
    print("Running inference ...")
    preds, true_labels = run_inference(
        model, tokenizer, test_df, args.max_length, args.batch_size, device
    )

    # ── Scalar metrics ─────────────────────────────────────────────────────
    acc = accuracy_score(true_labels, preds)

    prec_per_class, rec_per_class, f1_per_class, support = precision_recall_fscore_support(
        true_labels, preds, average=None,
        labels=list(range(len(labels))), zero_division=0,
    )
    prec_w, rec_w, f1_w, _ = precision_recall_fscore_support(
        true_labels, preds, average="weighted", zero_division=0
    )
    prec_m, rec_m, f1_m, _ = precision_recall_fscore_support(
        true_labels, preds, average="macro", zero_division=0
    )

    # ── Console report ─────────────────────────────────────────────────────
    sep = "=" * 62
    print(f"\n{sep}")
    print("TEST SET RESULTS")
    print(sep)
    print(f"  Accuracy           : {acc:.4f}")
    print(f"  Weighted Precision : {prec_w:.4f}")
    print(f"  Weighted Recall    : {rec_w:.4f}")
    print(f"  Weighted F1        : {f1_w:.4f}")
    print(f"  Macro Precision    : {prec_m:.4f}")
    print(f"  Macro Recall       : {rec_m:.4f}")
    print(f"  Macro F1           : {f1_m:.4f}")

    print(f"\n  {'Label':<15} {'Precision':>10} {'Recall':>10} {'F1':>10} {'Support':>10}")
    print(f"  {'-'*57}")
    for i, lbl in enumerate(labels):
        print(f"  {lbl:<15} {prec_per_class[i]:>10.4f} {rec_per_class[i]:>10.4f}"
              f" {f1_per_class[i]:>10.4f} {support[i]:>10}")

    report_str = classification_report(
        true_labels, preds, target_names=labels, digits=4, zero_division=0
    )
    print(f"\nClassification Report:\n\n{report_str}")

    # ── Save text report ───────────────────────────────────────────────────
    report_path = os.path.join(args.output_dir, "classification_report.txt")
    with open(report_path, "w") as fh:
        fh.write("TEST SET RESULTS\n")
        fh.write(sep + "\n")
        fh.write(f"Accuracy           : {acc:.4f}\n")
        fh.write(f"Weighted Precision : {prec_w:.4f}\n")
        fh.write(f"Weighted Recall    : {rec_w:.4f}\n")
        fh.write(f"Weighted F1        : {f1_w:.4f}\n")
        fh.write(f"Macro Precision    : {prec_m:.4f}\n")
        fh.write(f"Macro Recall       : {rec_m:.4f}\n")
        fh.write(f"Macro F1           : {f1_m:.4f}\n\n")
        fh.write("Classification Report:\n\n")
        fh.write(report_str)
    print(f"Report saved → {report_path}")

    # ── Save metrics CSV ────────────────────────────────────────────────────
    rows = [{"label": lbl, "precision": prec_per_class[i], "recall": rec_per_class[i],
             "f1": f1_per_class[i], "support": int(support[i])} for i, lbl in enumerate(labels)]
    rows.append({"label": "weighted avg", "precision": prec_w,
                 "recall": rec_w, "f1": f1_w, "support": len(true_labels)})
    rows.append({"label": "macro avg", "precision": prec_m,
                 "recall": rec_m, "f1": f1_m, "support": len(true_labels)})
    metrics_df = pd.DataFrame(rows)
    metrics_path = os.path.join(args.output_dir, "metrics.csv")
    metrics_df.to_csv(metrics_path, index=False)
    print(f"Metrics CSV saved → {metrics_path}")

    # ── Plots ──────────────────────────────────────────────────────────────
    cm = confusion_matrix(true_labels, preds)

    plot_confusion_matrix(
        cm, labels,
        os.path.join(args.output_dir, "confusion_matrix.png"),
    )
    plot_normalized_confusion_matrix(
        cm, labels,
        os.path.join(args.output_dir, "confusion_matrix_normalized.png"),
    )
    plot_per_class_metrics(
        prec_per_class, rec_per_class, f1_per_class, labels,
        os.path.join(args.output_dir, "per_class_metrics.png"),
    )

    print(f"\nAll outputs written to {args.output_dir}/")
    print("Evaluation complete.")


if __name__ == "__main__":
    main()

