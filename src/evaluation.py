import os
import argparse
import json
import numpy as np
import pandas as pd
import matplotlib
matplotlib.use("Agg")  # headless backend for script execution
import matplotlib.pyplot as plt
import seaborn as sns
import torch
from torch.utils.data import DataLoader
from datasets import Dataset
from sklearn.metrics import (
    classification_report,
    confusion_matrix,
    accuracy_score,
    precision_recall_fscore_support,
)
from transformers import (
    DistilBertTokenizerFast,
    DistilBertForSequenceClassification,
    DataCollatorWithPadding,
)

# Must match train.py
LABELS = ["declining", "emerging", "experimental", "mature"]
LABEL2ID = {label: idx for idx, label in enumerate(LABELS)}


def parse_args():
    parser = argparse.ArgumentParser(description="Evaluate fine-tuned DistilBERT on the test set")
    parser.add_argument("--model_dir", type=str, default="./model",
                        help="Directory containing the saved model and tokenizer")
    parser.add_argument("--test_path", type=str, default=None,
                        help="Path to test CSV (default: <model_dir>/test_set.csv)")
    parser.add_argument("--output_dir", type=str, default="./evaluation_results",
                        help="Where to save plots and reports")
    parser.add_argument("--batch_size", type=int, default=32)
    parser.add_argument("--max_length", type=int, default=512)
    return parser.parse_args()

# ---------------------------------------------------------------------------
# Inference
# ---------------------------------------------------------------------------

def run_inference(model, tokenizer, df: pd.DataFrame, max_length: int,
                  batch_size: int, device: str):
    """Tokenize df, run batched inference, return (predictions, true_labels)."""
    dataset = Dataset.from_dict({
        "text": df["text_representation"].tolist(),
        "labels": df["label_id"].tolist(),
    })

    def tokenize_batch(batch):
        return tokenizer(
            batch["text"],
            truncation=True,
            padding=False,
            max_length=max_length,
        )

    dataset = dataset.map(tokenize_batch, batched=True, remove_columns=["text"])
    dataset.set_format("torch")

    data_collator = DataCollatorWithPadding(tokenizer=tokenizer)
    loader = DataLoader(dataset, batch_size=batch_size, collate_fn=data_collator)

    all_preds, all_labels = [], []
    model.eval()
    with torch.no_grad():
        for batch in loader:
            true = batch.pop("labels").numpy()
            batch = {k: v.to(device) for k, v in batch.items()}
            logits = model(**batch).logits
            preds = torch.argmax(logits, dim=-1).cpu().numpy()
            all_preds.extend(preds)
            all_labels.extend(true)

    return np.array(all_preds), np.array(all_labels)

