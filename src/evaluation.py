def plot_confusion_matrix(cm: np.ndarray, labels: list, output_path: str):
    plt.figure(figsize=(8, 6))
    sns.heatmap(
        cm, annot=True, fmt="d", cmap="Blues",
        xticklabels=labels, yticklabels=labels,
        linewidths=0.5, linecolor="grey",
    )
    plt.title("Confusion Matrix — GitHub Repo Classification", fontsize=14, pad=12)
    plt.ylabel("True Label", fontsize=12)
    plt.xlabel("Predicted Label", fontsize=12)
    plt.xticks(rotation=30, ha="right")
    plt.yticks(rotation=0)
    plt.tight_layout()
    plt.savefig(output_path, dpi=150, bbox_inches="tight")
    plt.close()
    print(f"Saved → {output_path}")


def plot_normalized_confusion_matrix(cm: np.ndarray, labels: list, output_path: str):
    row_sums = cm.sum(axis=1, keepdims=True)
    cm_norm = np.where(row_sums == 0, 0.0, cm.astype(float) / row_sums)
    plt.figure(figsize=(8, 6))
    sns.heatmap(
        cm_norm, annot=True, fmt=".2f", cmap="YlOrRd",
        xticklabels=labels, yticklabels=labels,
        vmin=0, vmax=1, linewidths=0.5, linecolor="grey",
    )
    plt.title("Normalized Confusion Matrix (row %)", fontsize=14, pad=12)
    plt.ylabel("True Label", fontsize=12)
    plt.xlabel("Predicted Label", fontsize=12)
    plt.xticks(rotation=30, ha="right")
    plt.yticks(rotation=0)
    plt.tight_layout()
    plt.savefig(output_path, dpi=150, bbox_inches="tight")
    plt.close()
    print(f"Saved → {output_path}")


def plot_per_class_metrics(precision: np.ndarray, recall: np.ndarray,
                           f1: np.ndarray, labels: list, output_path: str):
    x = np.arange(len(labels))
    width = 0.25
    colors = ["#4C72B0", "#DD8452", "#55A868"]

    fig, ax = plt.subplots(figsize=(10, 6))
    bars_p = ax.bar(x - width, precision, width, label="Precision", color=colors[0])
    bars_r = ax.bar(x,         recall,    width, label="Recall",    color=colors[1])
    bars_f = ax.bar(x + width, f1,        width, label="F1-Score",  color=colors[2])

    for bars in (bars_p, bars_r, bars_f):
        for bar in bars:
            h = bar.get_height()
            ax.text(bar.get_x() + bar.get_width() / 2, h + 0.015,
                    f"{h:.2f}", ha="center", va="bottom", fontsize=8)

    ax.set_xlabel("Category", fontsize=12)
    ax.set_ylabel("Score", fontsize=12)
    ax.set_title("Per-class Metrics — GitHub Repo Classification", fontsize=14)
    ax.set_xticks(x)
    ax.set_xticklabels(labels, rotation=15, ha="right")
    ax.set_ylim(0, 1.18)
    ax.legend(fontsize=11)
    ax.grid(axis="y", alpha=0.3, linestyle="--")
    plt.tight_layout()
    plt.savefig(output_path, dpi=150, bbox_inches="tight")
    plt.close()
    print(f"Saved → {output_path}")

