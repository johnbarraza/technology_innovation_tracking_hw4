from __future__ import annotations

import math
import pandas as pd

# ---------------------------------------------------------------------------
# Helpers internos
# ---------------------------------------------------------------------------

def _fmt_num(value, decimals: int = 0) -> str:
    """Formatea un número con separadores de miles; maneja NaN/None."""
    try:
        if value is None or (isinstance(value, float) and math.isnan(value)):
            return "desconocido"
        if decimals == 0:
            return f"{int(value):,}".replace(",", ".")
        return f"{float(value):,.{decimals}f}".replace(",", "X").replace(".", ",").replace("X", ".")
    except (TypeError, ValueError):
        return "desconocido"


def _fmt_bool(value) -> str:
    try:
        return "sí" if bool(int(value)) else "no"
    except (TypeError, ValueError):
        return "desconocido"


def _safe(value, default="desconocido") -> str:
    if value is None:
        return default
    s = str(value).strip()
    return s if s and s.lower() not in ("nan", "none", "") else default


def _topics_text(topics) -> str:
    if topics is None:
        return "ninguno"
    if isinstance(topics, list):
        clean = [str(t).strip() for t in topics if str(t).strip()]
        return ", ".join(clean) if clean else "ninguno"
    s = str(topics).strip()
    if not s or s.lower() in ("nan", "none", "[]", ""):
        return "ninguno"
    s = s.strip("[]").replace("'", "").replace('"', "")
    parts = [p.strip() for p in s.split(",") if p.strip()]
    return ", ".join(parts) if parts else "ninguno"


def _classify_age(days) -> str:
    try:
        d = float(days)
        if d < 180:   return "muy nuevo (menos de 6 meses)"
        if d < 365:   return "nuevo (6–12 meses)"
        if d < 1095:  return "establecido (1–3 años)"
        if d < 2555:  return "maduro (3–7 años)"
        return "veterano (más de 7 años)"
    except (TypeError, ValueError):
        return "antigüedad desconocida"


def _classify_activity(days) -> str:
    try:
        d = float(days)
        if d <= 7:    return "muy reciente (<=7 días)"
        if d <= 30:   return "reciente (<=30 días)"
        if d <= 90:   return "moderada (1–3 meses)"
        if d <= 365:  return "baja (3–12 meses)"
        return "inactivo (más de un año)"
    except (TypeError, ValueError):
        return "actividad desconocida"


def _classify_velocity(vel) -> str:
    try:
        v = float(vel)
        if v >= 30:  return "muy alta"
        if v >= 15:  return "alta"
        if v >= 5:   return "moderada"
        if v >= 1:   return "baja"
        return "casi nula"
    except (TypeError, ValueError):
        return "desconocida"


def _classify_community(breadth) -> str:
    try:
        b = float(breadth)
        if b >= 0.8:  return "muy amplia"
        if b >= 0.5:  return "amplia"
        if b >= 0.3:  return "moderada"
        return "reducida"
    except (TypeError, ValueError):
        return "desconocida"


def _classify_recency(score) -> str:
    try:
        s = float(score)
        if s >= 0.8:  return "muy alto"
        if s >= 0.5:  return "alto"
        if s >= 0.3:  return "medio"
        return "bajo"
    except (TypeError, ValueError):
        return "desconocido"


def _readme_size(length) -> str:
    try:
        n = int(float(length))
        if n == 0:      return "sin README"
        if n < 500:     return f"breve ({n} caracteres)"
        if n < 3000:    return f"estándar ({n} caracteres)"
        return f"extenso ({n} caracteres)"
    except (TypeError, ValueError):
        return "desconocido"

