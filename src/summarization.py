def create_repository_summary(row) -> str:
    """
    Genera un resumen textual estructurado en español de un repositorio GitHub.

    Parameters
    ----------
    row : pandas.Series o dict-like
        Fila del DataFrame con las columnas del proyecto.

    Returns
    -------
    str
        Texto estructurado listo para ser procesado por un LLM clasificador.
    """

    # Identidad básica
    full_name   = _safe(row.get("full_name"),   "repositorio sin nombre")
    description = _safe(row.get("description"), "sin descripción disponible")
    language    = _safe(row.get("language"),    "no especificado")
    topics      = _topics_text(row.get("topics"))

    # Popularidad
    stars        = _fmt_num(row.get("stars"))
    forks        = _fmt_num(row.get("forks"))
    contributors = _fmt_num(row.get("contributors_count"))

    # Actividad reciente
    commits_30d = _fmt_num(row.get("commits_last_30d"))
    prs         = _fmt_num(row.get("pull_requests_count"))
    releases    = _fmt_num(row.get("releases_count"))
    has_ci      = _fmt_bool(row.get("has_ci"))

    # Métricas temporales
    repo_age = _fmt_num(row.get("repo_age_days"))
    last_act = _fmt_num(row.get("last_activity_days"))

    # Señales calculadas — texto legible
    age_label        = _classify_age(row.get("repo_age_days"))
    activity_label   = _classify_activity(row.get("last_activity_days"))
    vel_label        = _classify_velocity(row.get("commit_velocity"))
    recency_label    = _classify_recency(row.get("recency_score"))
    community_label  = _classify_community(row.get("community_breadth"))
    readme_label     = _readme_size(row.get("readme_length"))

    # Señales calculadas — valores numéricos formateados
    velocity_fmt        = _fmt_num(row.get("commit_velocity"),    decimals=1)
    recency_fmt         = _fmt_num(row.get("recency_score"),      decimals=3)
    release_cadence_fmt = _fmt_num(row.get("release_cadence"),    decimals=2)
    pr_throughput_fmt   = _fmt_num(row.get("pr_throughput"),      decimals=2)
    community_fmt       = _fmt_num(row.get("community_breadth"),  decimals=3)
    stars_growth_fmt    = _fmt_num(row.get("stars_growth_proxy"), decimals=4)

    summary = (
        f"=== RESUMEN DE REPOSITORIO GITHUB ===\n"
        f"\n"
        f"## Identificación\n"
        f"- Nombre completo: {full_name}\n"
        f"- Descripción: {description}\n"
        f"- Lenguaje principal: {language}\n"
        f"- Tópicos/etiquetas: {topics}\n"
        f"\n"
        f"## Popularidad y Alcance Social\n"
        f"- Estrellas: {stars}\n"
        f"- Forks: {forks}\n"
        f"- Contribuidores únicos: {contributors}\n"
        f"- Proxy de crecimiento de estrellas: {stars_growth_fmt}\n"
        f"\n"
        f"## Actividad de Desarrollo (Ventana Reciente)\n"
        f"- Commits en los últimos 30 días: {commits_30d}\n"
        f"- Pull requests totales: {prs}\n"
        f"- Releases publicadas: {releases}\n"
        f"- Integración continua (CI) configurada: {has_ci}\n"
        f"- Documentación (README): {readme_label}\n"
        f"\n"
        f"## Madurez Temporal\n"
        f"- Antigüedad del repositorio: {repo_age} días ({age_label})\n"
        f"- Días desde la última actividad registrada: {last_act} días — {activity_label}\n"
        f"\n"
        f"## Señales de Salud y Dinámica\n"
        f"- Velocidad de commits (promedio diario reciente): {velocity_fmt} commits/día — ritmo {vel_label}\n"
        f"- Puntuación de recencia: {recency_fmt} (nivel {recency_label})\n"
        f"- Cadencia de releases: {release_cadence_fmt} releases/mes\n"
        f"- Throughput de pull requests: {pr_throughput_fmt} PRs/mes\n"
        f"- Amplitud comunitaria (índice normalizado): {community_fmt} — comunidad {community_label}\n"
        f"\n"
        f"## Síntesis para Clasificación\n"
        f"Este repositorio ({full_name}) es un proyecto en {language} con {stars} estrellas "
        f"y {contributors} contribuidores. Su historial abarca {repo_age} días ({age_label}) "
        f"y su última actividad fue hace {last_act} días ({activity_label}). "
        f"La velocidad de commits es {vel_label} ({velocity_fmt} commits/día), "
        f"la cadencia de releases es {release_cadence_fmt}/mes y "
        f"la comunidad es {community_label}. "
        f"El score de recencia es {recency_label} ({recency_fmt}). "
        f"Basándote en estas métricas, clasifica este repositorio en una de las siguientes "
        f"categorías: [emerging, mature, declining, experimental].\n"
        f"\n"
        f"=== FIN DEL RESUMEN ==="
    )

    return summary

# ---------------------------------------------------------------------------
# Función batch
# ---------------------------------------------------------------------------

def batch_summarize(df: pd.DataFrame) -> pd.DataFrame:
    """
    Aplica create_repository_summary a cada fila del DataFrame y agrega
    la columna 'text_representation'.

    Parameters
    ----------
    df : pd.DataFrame

    Returns
    -------
    pd.DataFrame
        Copia con la columna 'text_representation' añadida.
    """
    result = df.copy()
    result["text_representation"] = result.apply(
        lambda row: create_repository_summary(row), axis=1
    )
    return result